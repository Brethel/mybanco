function showorhide (box) {
	switch (document.getElementById(box).style.visibility) {
		case '': document.getElementById(box).style.visibility="visible"; break
		case 'hidden': document.getElementById(box).style.visibility="visible"; break
		case 'visible': document.getElementById(box).style.visibility="hidden"; break
	}
}

